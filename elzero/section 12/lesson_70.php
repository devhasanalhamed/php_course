<?php

/*
Math functions

- ceil(Number[Required])
    - round up to integer

- floor
    - round down to integer
 */


$a = 3.2;
$b = 3.6;

$c = -2.4;

echo ceil($a) . "<br>";
echo ceil($b) . "<br>";

echo floor($a) . "<br>";
echo floor($b) . "<br>";

echo ceil($c) . "<br>";
echo floor($c) . "<br>";
